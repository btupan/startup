function ShowNavBar(){
    const testElement = document.querySelector('.navbar'); // Select the element with class 'test'
            
    // Check current display style and toggle
    if (testElement.style.display === 'none' || testElement.style.display === '') {
        testElement.style.display = 'flex'; // Change to display flex
    } else {
        testElement.style.display = 'none'; // Change back to none
    }

}
